class Trader:
    def run(self, state: TradingState):
        result = {}
        ts_orders = ORDERS.get(state.timestamp)
        if ts_orders:
            for symbol, order_list in ts_orders.items():
                if symbol not in state.order_depths:
                    continue
                result[symbol] = [
                    Order(symbol, int(p), int(q)) for p, q in order_list
                ]
        return result, 0, ""